public class Cachorro {
    private int id, age;
    private String name, race;

    public void setId(int id){
        this.id = id;
    }

    public int getId(){
        return this.id;
    }

    public void setName(String name){
        this.name = name;
    }

    public String getName(){
        return this.name; 
    }


    public void setRace(String race){
        this.race = race;
    }

    public String getRace(){
        return this.race;
    }

    public void setAge(int age){
        this.age = age;
    }

    public int getAge(){
        return this.age;
    }
}